def fun_1(a, b, c):
    """
    Function to add three input numbers - this time, with a 
    different description!
    
    :param a: a random param
    :type a: int
    :param b: another random param
    :type b: int
    :param c: yet another random param
    :type c: int
    :return: sum of three random params
    :rtype: int
    """

    sum = a + b + c
    return sum

def fun_2(a, b, c):
    """
    Function to multiply three parameters. This time with a different description again. Wow. 
    
    :param a: A random param again
    :type a: int
    :param b: another random param
    :type b: int
    :param c: the final random param
    :type c: int
    :return: product of three random params
    :rtype: int
    """

    product = a*b*c
    return product